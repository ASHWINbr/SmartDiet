package com.example.smartlog;

public class DietDetails {
	private String time,actualCalories, caloriesTaken, report;

	
	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getActualCalories() {
		return actualCalories;
	}

	public void setActualCalories(String actualCalories) {
		this.actualCalories = actualCalories;
	}

	public String getCaloriesTaken() {
		return caloriesTaken;
	}

	public void setCaloriesTaken(String caloriesTaken) {
		this.caloriesTaken = caloriesTaken;
	}

	public String getReport() {
		return report;
	}

	public void setReport(String report) {
		this.report = report;
	}

}
